package marketwatch;

public class ArrangedFrame
{
    protected double x;
    protected double y;
    protected double width;
    protected double height;
    
    public ArrangedFrame(double x,double y,double width,double height)
    {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
